    private void updateTechnicianComboBox(){
        // filling combo box with the existing Technicians 
        DefaultComboBoxModel c = (DefaultComboBoxModel)this.technicianCbx.getModel();
        c.removeAllElements();
        String idName ="";
        for(Technician t:this.lstTechnicians){
            //combine id and name of the technician to display in the combo box
            idName = t.getEmpId()+ ": " + t.getName();
            this.technicianCbx.addItem(idName);                                
        }
        this.technicianCbx.repaint();
             
        this.technicianCbx.setSelectedIndex(this.lstTechnicians.size()-1);
        
        if (this.technicianCbx.getItemCount()>0){
            this.statusTechnician = true;
            enableInstallationEntry(statusTechnician);
        }
        
        this.addressTextField.setFocusable(true);
     } 
    
