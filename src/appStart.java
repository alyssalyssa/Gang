    private void displayTechnician(){
        
        DefaultTableModel currentModel = (DefaultTableModel) this.displayTable.getModel();
        currentModel.setRowCount(0);
        Object [] row = new Object[3];
        for (int i = 0; i<this.lstTechnicians.size(); i++){
            //creating each row of Technician
            row[0] = this.lstTechnicians.get(i).getEmpId();
            row[1] = this.lstTechnicians.get(i).getName();
            row[2] = this.lstTechnicians.get(i).getPhoneNum();
            //adding the row to the table
            currentModel.addRow(row);
        }
        this.displayTable.setVisible(true);
        
    }
