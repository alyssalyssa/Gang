    private boolean checkInstallationFields(){
        //checking installation address is not blank and is not repeationg and technician is selected
        try{
            if(this.isTextFieldEmpty(addressTextField)){
                this.installationErrorLabel.setText("Enter address");
                return false;
            }
            if (existingInstallation(this.addressTextField.getText())){               
                return false;
            }
            if (this.technicianCbx.getSelectedIndex()<0){
                this.installationErrorLabel.setText("Select Technician");
                return false;
            }
        }catch(Exception e){
            this.installationErrorLabel.setText("Installation fields error");
        }
        return true;
    }
     private boolean existingInstallation(String aAddress){
        //installation address is checked for duplicate entry        
        for (Installation i: lstInstallations){ 
            if(i.getAddress().equalsIgnoreCase(aAddress))  { // new installation address is in the list
                this.installationErrorLabel.setText("Installation address is repeating");
                return true;
            }
        }
        return false; // new installation address is not in the list
    }
     private void addInstallationToList (Installation aInstallation){
         //new installation is added successfully
         this.installationErrorLabel.setText("Added new Installation");
         this.lstInstallations.add(aInstallation);
         
     }      
     private void displayInstallation (){
         
        DefaultTableModel currentModel = (DefaultTableModel) this.displayInstallationsTable.getModel();
        currentModel.setRowCount(0);
        Object [] row = new Object[6];
        for (int i = 0; i<this.lstInstallations.size(); i++){
            //creating and adding each row of installation to the table model
            row [0] = this.lstInstallations.get(i).getAddress().toString();
            row[1] = this.lstInstallations.get(i).getEmployee().getName();
            row[2] = this.lstInstallations.get(i).getNumZones();            
            row[3] = this.lstInstallations.get(i).getNumOutlets();
            row[4] = this.lstInstallations.get(i).getStartDate().toString();
            row[5] = this.lstInstallations.get(i).getEndDate().toString();
            currentModel.addRow(row);
        }
        this.displayInstallationsTable.setVisible(true);
         
     }      
    private void makeInstallation(Installation aInstallation){
        Technician selecetedTechnician = new Technician();
        //Verify the fields are not empty and then create a new installation and add it to the list
        if(checkInstallationFields()){
            try{
                String s1 = this.startDateTextField.getText();
                LocalDate pStartDate = LocalDate.parse(s1);
                aInstallation.setStartDate(pStartDate);
                String s2 = this.endDateTextField.getText();
                LocalDate pEndDate = LocalDate.parse(s2);
                pEndDate = LocalDate.parse(s2);
                if(pEndDate.compareTo(pStartDate)>=0) // checking end date is on or after the start date
                    aInstallation.setEndDate(pEndDate);
                else 
                    throw new Exception();
                if(this.technicianCbx.getSelectedIndex()!= -1)
                    selecetedTechnician = this.lstTechnicians.get(this.technicianCbx.getSelectedIndex());
                //setting the new installation's fields
                aInstallation.setAddress(this.addressTextField.getText());
                aInstallation.setEmployee(selecetedTechnician);
                aInstallation.setNumZones(Integer.parseInt(this.zoneSpinner.getValue().toString()));
                aInstallation.setNumOutlets(Integer.parseInt(this.outletSpinner.getValue().toString()));
                addInstallationToList(aInstallation);
                displayInstallation();  
            }catch (Exception e)
            {
                this.installationErrorLabel.setText("Error in date values");
            }
            
        }           
    }
